# === 4.1.1 问题改写模板（Query Rewrite） ===
# Token 预算：~200 token

QA_QUERY_REWRITE_TEMPLATE = """
请将以下学生提出的口语化问题，改写为用于知识库检索的精炼查询语句。

## 改写规则
1. 去除礼貌用语（"请问""老师""麻烦"等）和语气词
2. 提取核心概念和关键术语
3. 如果一个句子包含多个问题，拆分为 2-3 个独立查询（用换行分隔）
4. 保留原文中的代码/公式/专有名词（不翻译）
5. 改写后每条查询 ≤ 50 字
6. 如果原文就是精炼的术语查询，直接返回原文

## 示例
原始问题："老师你好，我想问一下递归函数和迭代函数到底有什么区别啊，能举例子说明吗？"
改写后：
递归和迭代的区别 示例
递归函数 定义 工作原理
迭代函数 定义 工作原理

---
原始问题：{question}
改写后：
"""


